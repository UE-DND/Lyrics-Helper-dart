class ArtistHelper {
  /// 中文化艺人名字符串
  static String chineselizeArtist(String? artist) {
    if (artist == null || artist.trim().isEmpty) return '';
    final pair = _artistNamePairs.firstWhere(
      (e) => e.name == artist,
      orElse: () => _Artist('', artist, artist),
    );
    return pair.chineseName;
  }

  /// 中文化艺人名字符串列表 (List 版本)
  static void chineselizeArtistsList(List<String> artists) {
    for (var i = 0; i < artists.length; i++) {
      artists[i] = chineselizeArtist(artists[i]);
    }
  }

  /// 中文化艺人名字符串数组 (Array 版本)
  static void chineselizeArtistsArray(List<String> artists) {
    for (var i = 0; i < artists.length; i++) {
      artists[i] = chineselizeArtist(artists[i]);
    }
  }

  /// 返回中文化后的新 List
  static List<String> toChineselizeArtistsList(List<String> artists) {
    return artists.map(chineselizeArtist).toList();
  }

